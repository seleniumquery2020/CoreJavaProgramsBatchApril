package operators;

// + - * / % Arithmetic Operators
public class ArithmeticOperator {
	
	public static void main(String[] args) {
		
		 int a = 10;
		 int b = 3;
		 int div = a/b;
		 System.out.println("Int Number Division = " +div);
		 
		 // % mod sign
		  int reminder = a % b;
		  System.out.println("Int Number Reminder = "+reminder);
		  
		  float a1 = 10.0f;
		  float b1 = 3.0f;
		  float division = a/b;
		  System.out.println("Float Number Division = " +division);
			 
			 // % mod sign
		  float rem = a % b;
		  System.out.println("Float Number Reminder = "+rem);
	}

}
