package operators;

public class UnaryOperator {

	public static void main(String[] args) {

		int i = 2;
		
		// i--; //Post Decrement
		i--;
		System.out.println(i);
		
		// i++; //post Increment
		i++;
		System.out.println(i);
		
		// ++i; //pre Increment
		System.out.println(++i);
		
		// --i; //Pre Decrement
		System.out.println(--i);
	}

}
